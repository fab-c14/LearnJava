class Literals{
    public static void main(String args[]){
        
        // literals
        // int num1 = 585;// how to create a binary number
        int num2 = 0b101;
        // int num3 = 1000000000;
        int num3 = 10_00_00_00; // feature of java , now we can count number of 0 easily
        System.out.println(num2); // ouput is 5
        System.out.println(num3);
        // double num4 =  10_00_00_00;// converted to double 
        double num5 = 12e10;// e stands for ebsalon
        System.out.println(num5);
        char c = 'a';
        System.out.println(c);
        // c = c+1;
        // here
        c++;
        System.out.println(c);


    }
}